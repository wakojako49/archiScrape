<h3 class="uk-modal-title"><strong>Professional Indemnity Insurance</strong></h3>
<p>This architect has applied for and obtained a professional indemnity insurance exemption from the Board for the current registration term. This exemption means that the architect is not required to hold professional indemnity insurance for the current registration term.</p>
<p><strong>If you are planning to engage this architect you should ensure that they hold appropriate professional indemnity insurance for the architectural services being provided to you.</strong></p>
<p>The NSW Architects Code of Professional Conduct provides for the Board to grant professional indemnity insurance exemptions in certain extenuating circumstances. Reasons for such exemptions may include, among other things, the architect being on parental leave; or being employed by a government agency, university or TAFE; or only practising overseas.</p>
<p><strong>The Board advises that any person planning to engage the services of an architect should request the architect to provide information relating to the insurance maintained by the architect.</strong></p>

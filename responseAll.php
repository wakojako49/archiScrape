<h1 class="title">Search Results</h1>
<p><strong>Architects</strong>
-
<strong>With Suburb or Postcode</strong> "2000"&nbsp;
</p>
<p>Architects in the practising category maintain appropriate professional indemnity insurance and undertake continuing professional development each year.</p>
<p>The NSW Architects Code of Professional Conduct (the Code) requires architects to provide details of their professional indemnity insurance to each client and clients should always ensure that their architect holds appropriate professional indemnity insurance for the architectural services being provided. Exemptions from professional indemnity insurance may be granted by the Board in certain extenuating circumstances.</p>
<p>Further <a class="architects" href="https://www.architects.nsw.gov.au/download/FACTSHEET%20-%20PROFESSIONAL%20INDEMNITY%20INSURANCE.pdf" target="_blank">information on PI Insurance can be found here</a>.</p>
<p>Automatic Mutual Recognition comes into effect in New South Wales on 1 December 2022. From that date, architects with the prefix 'DR' are registered under the 'deeming' provisions that apply for that scheme. Their 'home' jurisdiction is in another state or territory of Australia participating in the Automatic Mutual Recognition scheme.</p>
<p>Non-practising architects are not permitted to provide architectural services. They are retired or are otherwise not practising architecture and have undertaken to the Registrar that they will not provide architectural services in the current registration term. They are not required to carry professional indemnity insurance.</p>
<p>Please contact the Board on <strong>02 9241 4033</strong> if the architect you are looking for does not appear on the published list.</p>
